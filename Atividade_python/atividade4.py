# Exercício 4
cor = input("Digite uma cor (vermelho, verde, azul): ").lower()

if cor == "vermelho":
    print("Você escolheu a cor do fogo e da paixão!")
elif cor == "verde":
    print("Você escolheu a cor da natureza e da esperança!")
elif cor == "azul":
    print("Você escolheu a cor do céu e da tranquilidade!")
else:
    print("Cor não reconhecida.")